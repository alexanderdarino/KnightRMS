class ClassInfo
{
    private KnightEDU.Class classObj;
    private KnightEDU.Section section;

    public ClassInfo(Class classObj, Section section)
    {
        this.classObj = classObj;
        this.section = section;
    }

    public Class getClassObj()
    {
        return classObj;
    }

    public Section getSection()
    {
        return section;
    }

    @Override
    public String toString()
    {
        return "Class " + classObj.getID() + " Section " + section;
    }


}
