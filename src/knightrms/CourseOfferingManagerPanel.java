    private void ComponentListValueChanged(javax.swing.event.ListSelectionEvent evt)//GEN-FIRST:event_ComponentListValueChanged
    {//GEN-HEADEREND:event_ComponentListValueChanged
        int componentID = (Integer)ComponentList.getSelectedValue();


        //Get the set of components from the users courseID term and year
        Set<Integer> classIDs = db.getComponentClassIDs(componentID);
        ClassList.setModel(new DefaultListModel());
        Set<ClassInfo> classes = new HashSet();
        for (int i : classIDs)
        {
            KnightEDU.Class classObj = db.getClass(i);

            assert classObj != null;
            KnightEDU.Section section = db.getSection(classObj.getSectionID());
            classes.add(new ClassInfo(classObj, section));
        }
        ClassList.setListData(classes.toArray());
}//GEN-LAST:event_ComponentListValueChanged

    public void updateComponentsListing()
    {

        //Get the Course ID from the text field
        String CourseID = CourseIDTF.getText();

        //Get the term from the combo box
        KnightEDU.Term Term = KnightEDU.Term.valueOf(TermCB.getSelectedItem().toString());

        //Get the year from the text field
        int Year;
        if (YearTF.getText().isEmpty()) Year = 0;
            else Year = Integer.parseInt(YearTF.getText());

        //Get the set of components from the users courseID term and year
        courseOffering = db.getCourseOffering(CourseID, Term, Year);
        if (courseOffering == null)
        {
            ComponentList.setModel(new DefaultListModel());
            return;
        }

        Set<Integer> qComponent = db.queryComponent().getComponentIDs(courseOffering.getPrimaryComponentID());

        ComponentList.setListData(qComponent.toArray());
        classEditorCourseOfferingField.setText(courseOffering.toString());
    }

